// =========================================================
// ACTIONS — NAVIGATION UI
// =========================================================
// отвечает ТОЛЬКО за верхнюю навигацию
// никаких side-effect (save, fetch и т.д.)
// =========================================================

export function getNav(path) {

  // =====================================================
  // EDITOR
  // =====================================================
  // в редакторе НЕ показываем Save
  // потому что Save — это кнопка формы
  // =====================================================

  if (path === "/editor") {
    return `
      <a href="/" class="ui-link">Back</a>
    `;
  }

  // =====================================================
  // DEFAULT (index + pages)
  // =====================================================

  return `
    <a href="/editor" class="ui-link">New</a>
  `;
}
